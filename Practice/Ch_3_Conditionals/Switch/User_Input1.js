let choice=parseInt(prompt("1)Play\n2)Settings\n3)About\n4)Exit\nEnter the choice: "));
switch(choice){
    case 1:
        alert("The game is started!");
        break;
        case 2:
            alert("Setting page is opened!");
            break;
            case 3:
                alert("About page is opened!");
                break;
                case 4:
                    alert("The Game is Quitting!");
                    break;
                    default:
                        alert("You've entered wrong choice!");
}